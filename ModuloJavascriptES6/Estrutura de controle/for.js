//estrutura de repetição for

let contador = 1
// exemplo com while
while(contador <= 10){
    console.log(`valor do contador no while ${contador}`);
    contador++
}

// exemplo com for
//na extrutura do for, você pode criar, fazer a condição e incrementar ou decrementar a variavel criada
for(let cont=0; cont < 10; cont++){
    console.log(`valor do contador no for ${cont}`);
}